/*

El operador ternario es similar al uso de if else, va a tener una parte verdadera y otra falsa
se conoce como ternario porque consta de tres partes

la condicion booleana, la expresion de verdadero y la expresion de falso

Variable = condicion ? verdadero : falso;

*/
let resultado = (3>2) ? "Verdadero" : "falso";

console.log(resultado);

