let a = 3, b = 2;
let z = a + b;

//Incremento
//Preincremento ++variable

z = ++a;
console.log(a);
console.log(z);

//Postincremento variable++

z = b++;
console.log(b);
console.log(z);

//Decremento
//PreDecremento --variable

z = --a;
console.log(a);
console.log(z);

//PostDecremento variable--

z = b--;
console.log(b);
console.log(z);